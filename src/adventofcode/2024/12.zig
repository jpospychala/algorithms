const std = @import("std");

const Region = struct { area: usize, perimeter: usize };

fn fencePrice(in: []const u8, a: std.mem.Allocator) !usize {
    const mutIn = try a.alloc(u8, in.len);
    defer a.free(mutIn);
    @memcpy(mutIn, in);

    var ret: usize = 0;
    while (findRegion(mutIn)) |region| {
        ret += region.area * region.perimeter;
    }

    return ret;
}

fn findRegion(in: []u8) ?Region {
    const w: usize = std.mem.indexOfScalar(u8, in, '\n').? + 1;
    if (std.mem.indexOfNone(u8, in, ".\n")) |idx| {
        const c = in[idx];
        var region = Region{
            .area = 0,
            .perimeter = 0,
        };
        markRegion(in, w, c, @intCast(idx), &region);
        for (0..in.len) |i| {
            if (in[i] == '#') {
                in[i] = '.';
            }
        }
        std.debug.print("{s}\n\n", .{in});
        return region;
    }

    return null;
}

fn markRegion(in: []u8, w: usize, c: u8, idx: isize, region: *Region) void {
    if (idx < 0 or idx >= in.len or (in[@intCast(idx)] != c and in[@intCast(idx)] != '#')) {
        region.perimeter += 1;
    } else if (in[@intCast(idx)] == c) {
        region.area += 1;
        in[@intCast(idx)] = '#';
        markRegion(in, w, c, idx + 1, region);
        markRegion(in, w, c, idx - 1, region);
        markRegion(in, w, c, idx - @as(isize, @intCast(w)), region);
        markRegion(in, w, c, idx + @as(isize, @intCast(w)), region);
    }
}

